int	is_seperator(char c)
{
	if (c == '\n' || c == ' ' || c == '\0')
		return (1);
	return (0);
}

int	is_upper(char c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9'));
}

int	is_lower(char c)
{
	return ((c >= 'a' && c <= 'z') || (c >= '0' && c <= '9'));
}
